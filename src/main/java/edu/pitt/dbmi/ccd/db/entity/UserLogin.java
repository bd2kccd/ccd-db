package edu.pitt.dbmi.ccd.db.entity;

import java.io.Serializable;
import java.util.Date;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;
import javax.persistence.UniqueConstraint;

/**
 *
 * Feb 21, 2016 7:55:17 PM
 *
 * @author Kevin V. Bui (kvb2@pitt.edu)
 */
@Entity
@Table(name = "UserLogin", uniqueConstraints = @UniqueConstraint(columnNames = "userAccountId"))
public class UserLogin implements Serializable {

    private static final long serialVersionUID = 6378902686168445152L;

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "id", unique = true, nullable = false)
    private Long id;

    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "userAccountId", unique = true, nullable = false)
    private UserAccount userAccount;

    @Temporal(TemporalType.TIMESTAMP)
    @Column(name = "loginDate", nullable = false, length = 19)
    private Date loginDate;

    @Column(name = "loginLocation")
    private Long loginLocation;

    @Temporal(TemporalType.TIMESTAMP)
    @Column(name = "lastLoginDate", nullable = false, length = 19)
    private Date lastLoginDate;

    @Column(name = "lastLoginLocation")
    private Long lastLoginLocation;

    public UserLogin() {
    }

    public UserLogin(UserAccount userAccount, Date loginDate, Date lastLoginDate) {
        this.userAccount = userAccount;
        this.loginDate = loginDate;
        this.lastLoginDate = lastLoginDate;
    }

    public UserLogin(UserAccount userAccount, Date loginDate, Long loginLocation, Date lastLoginDate, Long lastLoginLocation) {
        this.userAccount = userAccount;
        this.loginDate = loginDate;
        this.loginLocation = loginLocation;
        this.lastLoginDate = lastLoginDate;
        this.lastLoginLocation = lastLoginLocation;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public UserAccount getUserAccount() {
        return userAccount;
    }

    public void setUserAccount(UserAccount userAccount) {
        this.userAccount = userAccount;
    }

    public Date getLoginDate() {
        return loginDate;
    }

    public void setLoginDate(Date loginDate) {
        this.loginDate = loginDate;
    }

    public Long getLoginLocation() {
        return loginLocation;
    }

    public void setLoginLocation(Long loginLocation) {
        this.loginLocation = loginLocation;
    }

    public Date getLastLoginDate() {
        return lastLoginDate;
    }

    public void setLastLoginDate(Date lastLoginDate) {
        this.lastLoginDate = lastLoginDate;
    }

    public Long getLastLoginLocation() {
        return lastLoginLocation;
    }

    public void setLastLoginLocation(Long lastLoginLocation) {
        this.lastLoginLocation = lastLoginLocation;
    }

}
